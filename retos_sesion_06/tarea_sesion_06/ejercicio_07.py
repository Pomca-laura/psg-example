# ¿El cuadrado y la suma de 2 y 2 son iguales?

# Calcular el cuadrado de 2
cuadrado_2 = 2 ** 2

# Calcular la suma de 2 y 2
suma_2_mas_2 = 2 + 2

# Verificar si el cuadrado y la suma son iguales
son_iguales = cuadrado_2 == suma_2_mas_2

# Imprimir el resultado
print("¿El cuadrado de 2 y la suma de 2 y 2 son iguales?:", son_iguales)
